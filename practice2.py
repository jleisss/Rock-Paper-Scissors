number1 = int(input("Please enter a number, this will be the first number represented in seperate equations using the four basic mathematical functions (+, -, *, /)  "))
number2 = int(input("Now enter another number, this will be the second number represented in seperate equations using the four basic mathematical funtions (+, -, *, /) "))

